package igl.vel.isr.appdef1_0.var_per;

public class stringblob {

    private String desc;
    private int img;

    public  stringblob()
    {

    }

    public stringblob(String desc, int img) {
        this.desc = desc;
        this.img = img;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
